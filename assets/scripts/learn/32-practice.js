const people = [
  '세종대왕',
  '이순신',
  '김구',
  '유관순',
  '장영실',
  '신사임당',
]

console.log(people)

/* ------------------------------------------------------------------
 1) '이순신'의 인덱스 찾기
------------------------------------------------------------------ */
const yiIndex = people.indexOf('이순신');
console.log('1️⃣  이순신 index:', yiIndex);           // 1

/* ------------------------------------------------------------------
 2) 배열 앞에 '정약용', '지석영' 추가
------------------------------------------------------------------ */
// 2-1. unshift()
people.unshift('정약용', '지석영');
console.log('2-1️⃣ unshift 후:', people);

// 2-2. splice()  → 0번째 위치에 0개 삭제 + 새 값 삽입
people.splice(0, 0, '정약용', '지석영');
// (위에서 이미 추가했으니 실습 땐 주석 처리하거나 people 리셋 후 테스트)

/* ------------------------------------------------------------------
 3) 배열 끝에 '김유신', '을지문덕' 추가
------------------------------------------------------------------ */
// 3-1. push()
people.push('김유신', '을지문덕');
console.log('3-1️⃣ push 후:', people);

// 3-2. splice()  → length 위치에 0개 삭제 + 새 값 삽입
// people.splice(people.length, 0, '김유신', '을지문덕');

/* ------------------------------------------------------------------
 4) 배열 앞에서 '세종대왕', '이순신' 제거
------------------------------------------------------------------ */
// 4-1. shift() 두 번
const removed1 = people.shift();   // 세종대왕
const removed2 = people.shift();   // 이순신
console.log('4-1️⃣ shift 후:', people, '| 제거:', removed1, removed2);

// 4-2. splice()  → 0번째부터 2개 삭제
// const removed = people.splice(0, 2);
// console.log('4-2️⃣ splice 후:', people, '| 제거:', removed);

/* ------------------------------------------------------------------
 5) 배열 끝에서 '장영실', '신사임당' 제거
------------------------------------------------------------------ */
// 5-1. pop() 두 번
const r1 = people.pop();           // 을지문덕
const r2 = people.pop();           // 김유신
console.log('5-1️⃣ pop 후:', people, '| 제거:', r1, r2);

// 5-2. splice()  → 뒤에서 2개 삭제
const removedTail = people.splice(-2);
console.log('5-2️⃣ splice 후:', people, '| 제거:', removedTail);

/* ------------------------------------------------------------------
 최종 결과
------------------------------------------------------------------ */

// 6-1. 복제 배열 생성 (얕은 복사)
const clonedPeople = [...people]; // 또는 people.slice()
console.log('복제된 배열:', clonedPeople);

// 6-2. '이순신', '김구'만 추출한 배열
const selectedPeople = people.filter(person =>
  person === '이순신' || person === '김구'
);

console.log('선택된 인물만:', selectedPeople); // ['이순신', '김구']
console.log('🏁 최종 people:', people);